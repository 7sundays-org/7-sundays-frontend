import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { join } from "node:path";
import { generateTypes } from "prismic-ts-codegen";

const root = process.cwd();

const readJSON = (p) => JSON.parse(readFileSync(p, "utf8"));

// Custom type models: customtypes/<id>/index.json
const customTypesDir = join(root, "customtypes");
const customTypeModels = readdirSync(customTypesDir, { withFileTypes: true })
  .filter((d) => d.isDirectory())
  .map((d) => readJSON(join(customTypesDir, d.name, "index.json")));

// Shared slice models: slices/<Name>/model.json
const slicesDir = join(root, "slices");
const sharedSliceModels = readdirSync(slicesDir, { withFileTypes: true })
  .filter((d) => d.isDirectory())
  .map((d) => readJSON(join(slicesDir, d.name, "model.json")));

const types = generateTypes({
  customTypeModels,
  sharedSliceModels,
  typesProvider: "@prismicio/client",
  clientIntegration: {
    includeCreateClientInterface: true,
    includeContentNamespace: true,
  },
});

const header = "// Code generated by Slice Machine. DO NOT EDIT.\n\n";
writeFileSync(join(root, "prismicio-types.d.ts"), header + types);

console.log(
  `Generated types for ${customTypeModels.length} custom types and ${sharedSliceModels.length} slices.`
);
